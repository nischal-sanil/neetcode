// Generates data/challenges/index.ts — a static barrel of every per-slug
// challenge file — so lib/data/content.ts can resolve challenges synchronously
// in a server component without runtime fs. Run in predev/prebuild.

import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dir = join(root, "data/challenges");

const slugs = readdirSync(dir)
  .filter((f) => f.endsWith(".json"))
  .map((f) => f.slice(0, -5))
  .sort();

const imports = slugs.map((slug, i) => `import c${i} from "./${slug}.json";`).join("\n");
const entries = slugs.map((slug, i) => `  ${JSON.stringify(slug)}: c${i} as Challenge,`).join("\n");

const out = `// AUTO-GENERATED by scripts/gen-challenge-index.mjs. Do not edit by hand.
import type { Challenge } from "@/lib/data/types";

${imports}

export const challenges: Record<string, Challenge> = {
${entries}
};
`;

writeFileSync(join(dir, "index.ts"), out);
console.log(`Wrote data/challenges/index.ts (${slugs.length} challenges)`);
