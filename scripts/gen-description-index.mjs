// Generates data/descriptions/index.ts — a static barrel of every per-slug
// description file — so lib/data/content.ts resolves descriptions synchronously
// in a server component. Run in predev/prebuild. Handles zero files.

import { existsSync, mkdirSync, readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const dir = join(root, "data/descriptions");
if (!existsSync(dir)) mkdirSync(dir, { recursive: true });

const slugs = readdirSync(dir)
  .filter((f) => f.endsWith(".json"))
  .map((f) => f.slice(0, -5))
  .sort();

const imports = slugs.map((slug, i) => `import d${i} from "./${slug}.json";`).join("\n");
const entries = slugs.map((slug, i) => `  ${JSON.stringify(slug)}: d${i} as Description,`).join("\n");

const out = `// AUTO-GENERATED by scripts/gen-description-index.mjs. Do not edit by hand.
import type { Description } from "@/lib/data/types";

${imports}

export const descriptions: Record<string, Description> = {
${entries}
};
`;

writeFileSync(join(dir, "index.ts"), out);
console.log(`Wrote data/descriptions/index.ts (${slugs.length} descriptions)`);
